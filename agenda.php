                    </br>- Des regards iconoclastes sur la révolution grise : à propos de la contribution des démographes belges à l’analyse du vieillissement. Jean-Michel Decroly, Marie Vandresse, Amandine Masuy (Société des Démographes Francophones de Belgique).
