    <h4>Août 2018</h4>
    <h4>Du 28 / 08 / 2018 Au 31 / 08 / 2018 </h4>
    <p>
      Colloque "Comment vieillissons-nous?"
      Du 28 au 31 août 2018 se déroulera conjointement à Louvain-la-Neuve
      le 20e colloque de l’Association Internationale des Démographes
      de Langue Française (AIDELF) et la 44e Chaire Quetelet du Centre de recherche...
