      <p>
        Du 28 au 31 août 2018 se déroulera conjointement à Louvain-la-Neuve le 20e colloque de l’Association Internationale des Démographes de Langue Française (AIDELF) et la 44e Chaire Quetelet du Centre de recherche en démographie (DEMO) de l’Université catholique de Louvain.
        </br>Le thème de ce colloque international est « Comment vieillissons-nous ? », une question d’une portée sociétale majeure pour les pays du Nord comme du Sud, mais aussi pour chaque personne soucieuse de préserver sa qualité de vie et sa dignité.
        </br>Le programme scientifique se compose d’une centaine de communications qui aborderont des thèmes tels que : les politiques publiques face à la dépendance, les enjeux territoriaux du vieillissement, vieillir en bonne santé, vieillir seul, vieillir en couple, la mortalité aux grands âges, les inégalités sociales de santé et de mortalité, les solidarités familiales et leurs limites, les relations intergénérationnelles ou encore forces et fragilités des liens sociaux.
        </br>Le programme scientifique du colloque et les informations pratiques (inscription, hébergement…) sont disponibles en suivant ce lien.
        </br>En lien avec le colloque et sa thématique, deux événements, gratuits et ouverts à tous publics, sont organisés.
        </br>Le 28 août, 13h00-16h00, Aula Magna (Foyer du Lac), Louvain-la-Neuve : Le vieillissement de la population en Belgique – un regard multi-disciplinaire, (organisation : AIDELF-DEMO (UCL), Louvain4 Ageing (UCL) et Société Démographique Francophone de Belgique)
        </br>- Des regards iconoclastes sur la révolution grise : à propos de la contribution des démographes belges à l’analyse du vieillissement. Jean-Michel Decroly, Marie Vandresse, Amandine Masuy (Société des Démographes Francophones de Belgique).
        </br>- Panorama démographique et conséquences économiques et sociales du vieillissement en Wallonie/Belgique. Mélanie Bourguignon, Catherine Gourbin, Jean-Paul Sanderson (Centre de recherche en Démographie-UCL).
        </br>- Augmenter l'âge de la retraite: la seule réponse possible au vieillissement ? Vincent Vandenberghe (Institut de recherches économiques et sociales, UCL ; Louvain4Ageing-UCL).
        </br>- Les résidents en maison de repos (MRPA) en Région de Bruxelles-Capitale : du libre choix à la contrainte. Sylvie Carbonnelle (Centre de Diffusion de la Culture Sanitaire).
        </br>- Vieillissement et santé vus sous l’angle du fonctionnement et de la fragilité. Christian Swine (Institut de recherche santé et société, UCL).
        </br>- L'implication des citoyens dans les choix en matière de gestion du vieillissement, Un panel prospectif citoyen au Parlement de Wallonie. Philippe Destatte (Institut Destrée).
        </br>- Présentation de l’enquête-pilote sur les 55 ans et plus à OLLN. Jean-Paul Sanderson, (Centre de recherche en Démographie-UCL ; Louvain4Ageing-UCL).
        </br>Le 29 août, de 16h30 à 18h00, Aula-Magna (Foyer du Lac), Louvain-la-Neuve, conférence-débat : Maintien à domicile : entre pragmatisme et idéologie, (organisation Association Internationale des Sociologues de Langue Française, AIDELF-DEMO (UCL)
        </br>Cette conférence-débat s’articulera autour du film « J’y suis j’y reste » et des interventions de Barbara Mazotti (Université de Genève), Ingrid Volery (Université de Lorraine) et des associations ENEO, FEMARBEL, FASD.
